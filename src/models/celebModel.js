import mongoose from 'mongoose';

var schema = new mongoose.Schema(
	{
		title: {
			type: String
		},
		desc: {
			type: String
		},
		pics: {
			type: Array,
			default: []
		},
		showIds: {
			type: Array,
			default: []
		}
	},
	{
		timestamps: true
	}
);

export default mongoose.models.celeb || mongoose.model('celeb', schema, 'celeb');
