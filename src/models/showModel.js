import mongoose from 'mongoose';

var schema = new mongoose.Schema(
	{
		title: {
			type: String
		},
		desc: {
			type: String
		},
		pics: {
			type: Array,
			default: []
		},
		rating: {
			type: Number
		},
		relDate: {
			type: Number
		},
		cert: {
			type: String
		},
		length: {
			type: Number
		},
		celebIds: {
			type: Array,
			default: []
		},
		cat: {
			type: Array,
			default: []
		}
	},
	{
		timestamps: true
	}
);

export default mongoose.models.show || mongoose.model('show', schema, 'show');
